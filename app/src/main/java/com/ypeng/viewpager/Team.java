package com.ypeng.viewpager;

public class Team {
    private String image;
    private String link;

    public Team(){
        // empty default constructor
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}